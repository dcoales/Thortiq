#!/usr/bin/env node
'use strict';

const {spawn} = require('child_process');
const crypto = require('crypto');
const fs = require('fs');
const path = require('path');
const jwt = require('jsonwebtoken');

const projectRoot = path.resolve(__dirname, '..');
const configPath = path.join(projectRoot, '.local-sync-config.json');
const envPath = path.join(projectRoot, 'apps', 'web-app', '.env.local');
const publicDir = path.join(projectRoot, 'apps', 'web-app', 'public');
const publicConfigPath = path.join(publicDir, 'local-sync.json');

const defaultConfig = {
  secret: crypto.randomBytes(24).toString('hex'),
  userId: 'local-user',
  displayName: 'Local User',
  docId: 'thortiq-outline',
  wsUrl: 'ws://localhost:5001',
  httpUrl: 'http://localhost:5001'
};

const loadConfig = () => {
  if (!fs.existsSync(configPath)) {
    return {...defaultConfig};
  }
  try {
    const parsed = JSON.parse(fs.readFileSync(configPath, 'utf8'));
    return {...defaultConfig, ...parsed};
  } catch (error) {
    console.warn('Failed to parse existing local sync config, regenerating.');
    return {...defaultConfig};
  }
};

const saveConfig = (config) => {
  fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
};

const writeEnvFile = (config, token) => {
  const desired = new Map([
    ['VITE_SYNC_SERVER_URL', config.wsUrl],
    ['VITE_SYNC_HTTP_URL', config.httpUrl],
    ['VITE_SYNC_DOC_ID', config.docId],
    ['VITE_SYNC_TOKEN', token]
  ]);
  const desiredKeys = new Set(desired.keys());

  let preserved = [];
  if (fs.existsSync(envPath)) {
    const lines = fs.readFileSync(envPath, 'utf8').split(/\r?\n/);
    preserved = lines.filter((line) => {
      const trimmed = line.trim();
      if (trimmed === '' || trimmed.startsWith('#')) {
        return true;
      }
      const [key] = trimmed.split('=', 1);
      return key && !desiredKeys.has(key);
    });
  }

  const outputLines = [
    '# Auto-generated by scripts/start-local-sync-server.js',
    '# Adjust values as needed for local development.',
    ...preserved.filter((line) => line.trim().length > 0),
    ...Array.from(desired.entries()).map(([key, value]) => `${key}=${value}`)
  ];

  fs.writeFileSync(envPath, `${outputLines.join('\n')}\n`);
};

const config = loadConfig();
const token = jwt.sign(
  {
    sub: config.userId,
    name: config.displayName
  },
  config.secret,
  {expiresIn: '12h'}
);

config.lastGeneratedToken = {
  issuedAt: new Date().toISOString(),
  expiresIn: '12h'
};

saveConfig(config);
writeEnvFile(config, token);

fs.mkdirSync(publicDir, {recursive: true});
const bootstrapPayload = {
  serverUrl: config.wsUrl,
  httpUrl: config.httpUrl,
  docId: config.docId,
  token,
  issuedAt: new Date().toISOString(),
  expiresIn: '12h'
};
fs.writeFileSync(publicConfigPath, `${JSON.stringify(bootstrapPayload, null, 2)}\n`);

console.log('Local sync configuration');
console.log(`  Secret file: ${configPath}`);
console.log(`  JWT (12h): ${token}`);
console.log(`  Web client env: ${envPath}`);
console.log(`  Web client bootstrap: ${publicConfigPath}`);
console.log('Press Ctrl+C to stop the sync server.');

const child = spawn(
  'npm',
  ['run', 'start', '--workspace', '@thortiq/sync-server'],
  {
    cwd: projectRoot,
    stdio: 'inherit',
    env: {
      ...process.env,
      THORTIQ_JWT_SECRET: config.secret
    }
  }
);

const shutdown = (signal) => {
  if (child.killed) {
    process.exit(0);
    return;
  }
  child.kill(signal);
};

process.on('SIGINT', () => shutdown('SIGINT'));
process.on('SIGTERM', () => shutdown('SIGTERM'));

child.on('exit', (code, signal) => {
  if (signal) {
    process.exit(0);
    return;
  }
  process.exit(code ?? 0);
});
